using System;

class Program {
    static void Main(string[] args) {
        int[,] array = new int[4, 4]; // создаем двумерный массив размером 4x4

        int value = 1; // начальное значение для заполнения массива
        int row = 0; // начальное значение для строки
        int col = 0; // начальное значение для столбца
        int n = 4; // количество строк и столбцов в массиве

        // заполняем массив в спиральном порядке
        for (int i = 0; i < n / 2; i++) {
            for (int j = i; j < n - i - 1; j++) {
                array[i, j] = value++; // заполняем верхнюю горизонтальную линию
            }
            for (int j = i; j < n - i - 1; j++) {
                array[j, n - i - 1] = value++; // заполняем правую вертикальную линию
            }
            for (int j = n - i - 1; j > i; j--) {
                array[n - i - 1, j] = value++; // заполняем нижнюю горизонтальную линию
            }
            for (int j = n - i - 1; j > i; j--) {
                array[j, i] = value++; // заполняем левую вертикальную линию
            }
        }

        // выводим массив на консоль
        for (int i = 0; i < n; i++) {
            for (int j = 0; j < n; j++) {
                Console.Write($"{array[i, j],2} "); // выводим элемент массива с отступом в 2 символа
            }
            Console.WriteLine(); // переходим на новую строку
        }
    }
}
